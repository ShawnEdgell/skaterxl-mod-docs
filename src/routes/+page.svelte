  </div>
  <p>
    <i>
      If you don't see Ingame Version: 0.27.X.X, you may have selected the wrong game folder or forgot to click Install.
    </i>
  </p>
  <h3>Step 3</h3>
  <p class="mt-2"><b>Download</b> mods. You can use the sidebar to find every working mod in Skater XL.</p>
  <p><b>Drag & Drop</b> your mod .zip files into UnityModManager. This will complete the installation process and they will now be usable in-game!</p>
  <div class="max-w-4xl mx-auto py-4">
