  <p>
    <i>In this example we are saving UnityModManager to our Desktop. You can save it anywhere you want, just make sure you know where it is.
    </i>
  </p>
  <h3>Step 2</h3>
  <p>
    Open the UnityModManager.exe file. This will open a window that looks like the one below.
  </p>
    <p class="mt-2">Set <b>Game</b> to Skater XL from the drop down menu.</p>
    <p>Set <b>Folder</b> to your Skater XL game folder, usually located here:</p>
    <p class="m-2">
      <i>This PC > Local Disk (C:) > ProgramFiles(x86) > Steam > steamapps > common > Skater XL
      </i>
    </p>
    <p>Click <b>Install</b> or <b>Update.</b> You should now see Ingame Version: 0.27.X.X. This means you're UnityModManager is set up and you can go ahead and install your mods.  </p>
    <p class="m-2">
  <div class="max-w-4xl mx-auto py-4">
