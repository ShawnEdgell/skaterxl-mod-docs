  <p>
    <i>All of your installed mods will say <b>Status: OK</b> or <b>Status: Update X.X.X,</b> this means the mod is installed properly. If this section is blank, the mod is not installed in your game. You can right-click each mod to manually install/uninstall.
    </i>
  </p>
  <p class="mt-2">
  ‚ö†Ô∏è <b>Do not 'Update' your mods here unless you are sure you are switching to the correct version. This can cause your mods to break and you will have to reinstall them.
