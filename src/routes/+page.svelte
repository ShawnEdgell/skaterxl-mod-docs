  <h3>Step 2</h3>
  <p>
    Select the <b>Betas</b> tab and choose the version you want to play from the drop-down menu.
  </p>
  <ul>
    <li>None = Skater XL v1.2.7.8 (Public)
      <i>- Default version</i>
    </li>
    <li>Alpha = Skater XL v1.2.2.8
      <i>- Recommended version</i>
    </li>
    <li>Beta = Skater XL v1.2.6.0
      <i>- Outdated, consider switching to either None or Alpha.
        </i>
      </li>
