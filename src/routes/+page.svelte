  <h2>Download & Setup UnityModManager</h2>
  <p>
    For Skater XL version 1.2.2.8: <a class="text-blue-600" href="https://www.nexusmods.com/site/mods/21?tab=files&file_id=2224" target="_blank" rel="noopener noreferrer">UnityModManager v0.27.4</a>
  </p>
  <p>
    For Skater XL version 1.2.7.8: <a class="text-blue-600" href="https://www.nexusmods.com/site/mods/21?tab=files&file_id=2314" target="_blank" rel="noopener noreferrer">UnityModManager v0.27.7</a>
  </p>
  <p class="mt-2">
    ‚ö†Ô∏è <i>Choosing the right version is mandatory for your mods to work. Delete your old mod manager before using one of these.
    </i>
  </p>

  <h3>Step 1</h3>
  <p>After you have the .zip file downloaded, you can right-click it and select Extract All. This will create a new folder with the same name as the .zip file. You can then open the folder and proceed to the next step.
  </p>

  <div class="max-w-4xl mx-auto py-4">
    <!-- Your content here -->
  
    <img src="UMM1.png" alt="Descriptive Alt Text" />
  
    <!-- More content -->
