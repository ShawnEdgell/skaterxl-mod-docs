  <h1>Maps and Gear for Skater XL</h1>

  <h2>Overview</h2>
  <p>
    Skater XL offers an expansive world of custom maps and gear created by the community. They can be easily downloaded and added to your game for an enhanced and varied skating experience.
  </p>

  <h2>How to Install Custom Maps</h2>
  <p>
    To install custom maps, simply download your desired map files and place them in the 'Maps' folder within your 'Documents/Skater XL' directory. This allows the game to recognize and load these maps. You can find a variety of maps on platforms like Mod.io or community-run Discord servers.
  </p>

  <h2>How to Install Custom Gear</h2>
  <p>
    Installing new gear is as simple as downloading texture files and placing them into the 'Gear' folder in your 'Documents/Skater XL' directory. For more advanced customizations like backpacks, dynamic clothing, and custom characters, the XL Gear Modifier (XLGM) is your go-to tool.
  </p>

  <h2>Community Creations</h2>
  <p>
    The Skater XL community actively creates and shares a plethora of mods including both maps and gear. These creations add depth and variety to the game, keeping it fresh and exciting. Remember to enable 'User Generated Content' in your game settings to make the most out of these community creations.
  </p>

  <p>
    Note: If you're missing the 'Maps' and 'Gear' folders in your directory, installing maps and gear directly through the game's mod browser should automatically create these folders for you.
  </p>

