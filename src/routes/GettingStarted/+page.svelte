  <h1>Getting Started</h1>
  <p>Welcome to the world of Skater XL modding! Follow these steps to begin your modding journey.</p>

  <h2>1. Confirm Your Skater XL Version</h2>
  <p>It's crucial to know which version of Skater XL you're installing mods into, as mod compatibility depends on your game version. Both the Alpha and Public branches are viable for modding, but we recommend version v1.2.2.8 (Alpha) due to its extensive mod support.</p>
  <div>
      <p>Learn more about each version:</p>
      <ul>
          <li><a href="/Alpha/About" class="font-semibold">Skater XL v1.2.2.8 (Alpha)</a> - Best mod support</li>
          <li><a href="/Public/About" class="font-semibold">Skater XL v1.2.7.8 (Public)</a> - Default Steam version, also moddable</li>
      </ul>
  <p>
    If your game version does not match your desired version, please follow the instructions below to switch.</p>
  </div>  
  <img src="XLversion.png" alt="Game Version Display" class="my-4" />
  <div class="note">
      <p class=""><strong>Note:</strong> You can easily check your current game version in the lower left corner upon launching Skater XL.</p>
  </div>

  <h2>2. Changing Skater XL Versions</h2>
  <p>Switching between Skater XL versions is straightforward via Steam:</p>
  <ol class="list-decimal ml-4">
    <li>Right-click Skater XL in your Steam library > Properties.</li>
    <li>Select the <b>Betas</b> tab and choose the desired version from the dropdown.</li>
  </ol>
  <p>Versions available:</p>
  <ul class="list-disc ml-6">
    <li><b>None</b> - Skater XL v1.2.7.8 (Public), the default version</li>
    <li><b>Alpha</b> - Skater XL v1.2.2.8, recommended for modding</li>
    <li><b>Beta</b> - Skater XL v1.2.6.0, outdated</li>
  </ul>
  <img src="XLDowngrade2.png" alt="Version Selection" class="my-4" />

  <h2>3. Download & Setup UnityModManager</h2>
  <p>UnityModManager is essential for installing and managing Skater XL mods. You will need to create an account and download the correct version based on your game:</p>
  <ul>
    <li>v1.2.2.8: <a href="https://www.nexusmods.com/site/mods/21?tab=files&file_id=2224" target="_blank" rel="noopener noreferrer">UnityModManager v0.27.4</a></li>
    <li>v1.2.7.8: <a href="https://www.nexusmods.com/site/mods/21?tab=files&file_id=2314" target="_blank" rel="noopener noreferrer">UnityModManager v0.27.7</a></li>
  </ul>
  
  <div class="warning ">
    <p><strong>⚠️ Important:</strong> Ensure you download the UnityModManager version that matches your Skater XL version. Failure to do so may result in mods not working. Uninstall any previous mod manager versions before installing a new one.</p>
  </div>
  
  <h3 class="pb-2">Installation Steps:</h3>
  <ol class="list-decimal ml-4">
    <li><strong>Extract UnityModManager:</strong> After downloading, extract the .zip file. Open this folder to access UnityModManager.exe.</li>
    <li><strong>Setup UnityModManager:</strong> Run UnityModManager.exe, set the game to Skater XL, and direct it to your game folder. For most users, the game folder is located here:</li>
      <p><i>This PC > Local Disk (C:) > Program Files (x86) > Steam > steamapps > common > Skater XL</i>
      <p>Click "Install" or "Update" to complete setup.</p>
      <img src="UMM2.png" alt="UnityModManager Setup" class="my-4" />
      <li><strong>Download and Install Mods:</strong> To acquire mods, navigate through our sidebar which grants convenient access to a comprehensive list of mods compatible with your game. Simply drag and drop the downloaded .zip mod files into the "Mods" tab of UnityModManager for installation.</li>
      <img src="UMM3.png" alt="UnityModManager Setup" class="my-4" />
      <div class="note">
        <p><strong>Note:</strong> In UnityModManager, <b>"Status: OK"</b> or <b>"Status: Update to X.X.X"</b> indicates a mod is installed. Avoid updating mods directly from UnityModManager, as changing versions may render them incompatible. For manual installation or uninstallation, right-click each mod.</p>
      </div>
      <div class="warning ">
        <p><strong>⚠️ Troubleshooting:</strong> If you get <b>"method on enter"</b> or <b>"method on start"</b> in UnityModManager, it's because you have the wrong version of UMM. You will need to delete your UnityModManager and download the right one for your game.  </p>
      </div>
    </ol>
      <h2>4. Launch Game</h2>
      <p>Start Skater XL. UnityModManager will open, indicating mod status. A green icon suggests the mod is working, while a red icon indicates issues.</p>
  <img src="UMM4.png" alt="Launch Game" class="my-4" />
  
